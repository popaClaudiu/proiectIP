void patrat(int x,int y,int lat)
{
    line(x-lat/2,y-lat/2,x-lat/2,y+lat/2);
    line(x-lat/2,y+lat/2,x+lat/2,y+lat/2);
    line(x+lat/2,y+lat/2,x+lat/2,y-lat/2);
    line(x+lat/2,y-lat/2,x-lat/2,y-lat/2);
}
