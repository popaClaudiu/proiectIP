bool esteInegalitateSauEgalitate(char *expresie)
{
    int contor = 0;
    for(int i = 0; i < strlen(expresie); i++){
        if(strchr("=><#",expresie[i])){
            contor++;
        }
    }

    if(contor == 1) return true;
    return false;

}

